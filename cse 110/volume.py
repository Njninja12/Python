length = int(input("Type the length (in cm): "))
area = length ** 2
print(f"The area of the cube is: {area} cm^2")
print()
print(f"The area of a square is: {length ** 2} cm^3")
print()
import math
print(f"The area of a circle is: {math.pi * length ** 2}")
print()
print(f"The volume of a sphere is: {(4 / 3) * math.pi * length ** 3}")
